package com.tianwen.commons.questionnaire.response;

import java.util.List;

public class QuestionResponse {

    private Integer id; //问题ID
    
    private String title; //问题题目
    
    private String questionType; //问题类型 S/M/F
    
    private List<ChooseResponse> choose; //选项

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public List<ChooseResponse> getChoose() {
        return choose;
    }

    public void setChoose(List<ChooseResponse> choose) {
        this.choose = choose;
    }

    public String getQuestionType() {
        return questionType;
    }

    public void setQuestionType(String questionType) {
        this.questionType = questionType;
    }
}
