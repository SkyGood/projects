package com.suse.all.response;

public class AllResponse {

	private Integer id;

	private String honerName;

	private Integer ticketNums;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getHonerName() {
		return honerName;
	}

	public void setHonerName(String honerName) {
		this.honerName = honerName;
	}

	public Integer getTicketNums() {
		return ticketNums;
	}

	public void setTicketNums(Integer ticketNums) {
		this.ticketNums = ticketNums;
	}

}
